    public class Person{
        private String name;
        private String surname;
        private int age;
        private char genger;

        public Person(String name, String surname){
            this.name = name;
            this.surname = name;

        }
        public Person(String name, int age){
            this.name = name;
            this.age = age;
        }
        public Person(String name, String surname, int age, char genger){
            this.name = name;
            this.surname = surname;
            this.age = age;
            this.genger = genger;

        }

    }
